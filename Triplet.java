public class Triplet {
	private Object _one;
	private Object _two;
	private Object _three;
	
	public Triplet(Object one, Object two, Object three) {
		_one = one;
		_two = two;
		_three = three;
	}
	
	public Object getOne() {
		return _one;
	}
	
	public Object getTwo() {
		return _two;
	}
	
	public Object getThree() {
		return _three;
	}
}
